package com.day1.optionalclass.studentmanagement;

class Student {
    int id;
    String name;
    String email;
    String phone;
    String address;
    String nickname;

    Student(int id, String name, String email, String phone, String address, String nickname) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
        this.nickname = nickname;
    }
}

